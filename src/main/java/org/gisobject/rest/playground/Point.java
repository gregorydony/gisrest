package org.gisobject.rest.playground;

/**
 * Created by GIS Object on 23/02/2018.
 */
public class Point {

    private double x;

    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
